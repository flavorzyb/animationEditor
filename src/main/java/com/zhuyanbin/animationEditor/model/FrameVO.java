package com.zhuyanbin.animationEditor.model;

final public class FrameVO
{
    private int _x;
    private int _y;
    private int _width;
    private int _height;
    
    public FrameVO()
    {
    }

    public int getX()
    {
        return _x;
    }

    public void setX(int x)
    {
        _x =x;
    }

    public int getY()
    {
        return _y;
    }

    public void setY(int y)
    {
        _y = y;
    }

    public int getWidth()
    {
        return _width;
    }

    public void setWidth(int width)
    {
        _width = width;
    }

    public int getHeight()
    {
        return _height;
    }

    public void setHeight(int height)
    {
        _height = height;
    }
}
